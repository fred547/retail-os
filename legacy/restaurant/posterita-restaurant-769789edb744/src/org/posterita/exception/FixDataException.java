package org.posterita.exception;

public class FixDataException extends Exception {
	
	public FixDataException() {
		super();
	}
	
	public FixDataException(String message) {
		super(message);
	}
	
	public FixDataException(Throwable cause) {
		super(cause);
	}
	
	public FixDataException(String message, Throwable cause) {
		super(message, cause);
	}

}
