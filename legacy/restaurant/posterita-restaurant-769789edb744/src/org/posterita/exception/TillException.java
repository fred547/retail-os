package org.posterita.exception;

public class TillException extends Exception {
	
	public TillException() {
		super();
	}
	
	public TillException(String message) {
		super(message);
	}
	
	public TillException(Throwable cause) {
		super(cause);
	}
	
	public TillException(String message, Throwable cause) {
		super(message, cause);
	}

}
