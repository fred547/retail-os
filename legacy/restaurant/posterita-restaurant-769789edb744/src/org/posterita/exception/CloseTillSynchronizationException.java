package org.posterita.exception;

public class CloseTillSynchronizationException extends Exception {
	
	public CloseTillSynchronizationException() {
		super();
	}
	
	public CloseTillSynchronizationException(String message) {
		super(message);
	}
	
	public CloseTillSynchronizationException(Throwable cause) {
		super(cause);
	}
	
	public CloseTillSynchronizationException(String message, Throwable cause) {
		super(message, cause);
	}

}
