package org.posterita.exception;

public class ClockInOutSynchronizationException extends Exception {
	
	public ClockInOutSynchronizationException() {
		super();
	}
	
	public ClockInOutSynchronizationException(String message) {
		super(message);
	}
	
	public ClockInOutSynchronizationException(Throwable cause) {
		super(cause);
	}
	
	public ClockInOutSynchronizationException(String message, Throwable cause) {
		super(message, cause);
	}

}
