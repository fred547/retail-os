package org.posterita.exception;

public class ReinstallationAccountException extends Exception {
	
	public ReinstallationAccountException() {
		super();
	}
	
	public ReinstallationAccountException(String message) {
		super(message);
	}
	
	public ReinstallationAccountException(Throwable cause) {
		super(cause);
	}
	
	public ReinstallationAccountException(String message, Throwable cause) {
		super(message, cause);
	}

}
