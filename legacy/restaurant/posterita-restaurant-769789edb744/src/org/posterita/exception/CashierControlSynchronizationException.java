package org.posterita.exception;

public class CashierControlSynchronizationException extends Exception {
	
	public CashierControlSynchronizationException() {
		super();
	}
	
	public CashierControlSynchronizationException(String message) {
		super(message);
	}
	
	public CashierControlSynchronizationException(Throwable cause) {
		super(cause);
	}
	
	public CashierControlSynchronizationException(String message, Throwable cause) {
		super(message, cause);
	}

}
