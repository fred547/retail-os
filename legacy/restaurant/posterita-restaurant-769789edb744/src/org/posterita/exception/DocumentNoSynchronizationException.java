package org.posterita.exception;

public class DocumentNoSynchronizationException extends Exception {
	
	public DocumentNoSynchronizationException() {
		super();
	}
	
	public DocumentNoSynchronizationException(String message) {
		super(message);
	}
	
	public DocumentNoSynchronizationException(Throwable cause) {
		super(cause);
	}
	
	public DocumentNoSynchronizationException(String message, Throwable cause) {
		super(message, cause);
	}

}
